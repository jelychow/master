package com.sec.hidinner.sociation;

public class Courier {

    private String name;
    private String id;
    private String gendar;
    private String age;
    private String characterSignature;
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getId() {
        return id;
    }
    public void setId(String id) {
        this.id = id;
    }
    public String getGendar() {
        return gendar;
    }
    public void setGendar(String gendar) {
        this.gendar = gendar;
    }
    public String getAge() {
        return age;
    }
    public void setAge(String age) {
        this.age = age;
    }
    public String getCharacterSignature() {
        return characterSignature;
    }
    public void setCharacterSignature(String characterSignature) {
        this.characterSignature = characterSignature;
    }
}
